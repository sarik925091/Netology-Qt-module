void MainWindow::on_pushButton_clicked()
{
    if(PB_counter == 100)
    {
        ui->progressBar->setValue(0);
        PB_counter = 0;
    }
    else
    {
        ui->progressBar->setValue(PB_counter += 10);
    }
    QString zodiac = ui->CB_zodiac->currentText();
    QString text = set_description_zodiac(zodiac);
    ZD_ui->setDescription(text);
    ZD_ui->show();
}


